ai_client = genai.Client(api_key=GEMINI_API_KEY)

intents = discord.Intents.default()
intents.message_content = True
client = discord.Client(intents=intents)

@client.event
async def on_ready():
    print('Джарвис успешно запущен!')

@client.event
async def on_message(message):
    if message.author == client.user:
        return

    # Читаем абсолютно любое сообщение в чате
    user_text = message.content.strip()

    # На случай, если вы всё-таки решите его тегнуть, вырезаем тег
    if client.user.mentioned_in(message):
        user_text = user_text.replace(f'<@{client.user.id}>', '')
        user_text = user_text.replace(f'<@!{client.user.id}>', '')
        user_text = user_text.strip()

    if not user_text:
        return

    try:
        response = ai_client.models.generate_content(
            model='gemini-2.5-flash',
            contents=user_text,
            config=types.GenerateContentConfig(
                system_instruction=JARVIS_PROMPT
            )
        )
        await message.channel.send(response.text)
    except Exception as e:
        print(f"Ошибка ИИ: {e}")
